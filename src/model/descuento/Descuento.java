package model.descuento;

/**
 *
 * @author fernandacancinoreyes
 */
public class Descuento {
    
    private int idDescuento;
    private int minPuntos;
    private int maxPuntos;
    private int porcentajeDescuento;
    private int topeDescuento;

    public int getIdDescuento() {
        return idDescuento;
    }

    public void setIdDescuento(int idDescuento) {
        this.idDescuento = idDescuento;
    }

    public int getMinPuntos() {
        return minPuntos;
    }

    public void setMinPuntos(int minPuntos) {
        this.minPuntos = minPuntos;
    }

    public int getMaxPuntos() {
        return maxPuntos;
    }

    public void setMaxPuntos(int maxPuntos) {
        this.maxPuntos = maxPuntos;
    }

    public int getPorcentajeDescuento() {
        return porcentajeDescuento;
    }

    public void setPorcentajeDescuento(int porcentajeDescuento) {
        this.porcentajeDescuento = porcentajeDescuento;
    }

    public int getTopeDescuento() {
        return topeDescuento;
    }

    public void setTopeDescuento(int topeDescuento) {
        this.topeDescuento = topeDescuento;
    }
    
    
    
    
}
