    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

      
        if(txtNombreEpresa.getText().trim().length() != 0 && txtCalleDireccion.getText().trim().length() != 0
            && txtObservacion.getText().trim().length() != 0 && txtNumero.getText().trim().length() != 0
        )//este if es para validar algunos campos vacios
        {
            
            
                try {

                    Connection cn = obj.ConnectBd();
                    String sql = "Insert into EMPRESA (nombreempresa,idusuario," +
                    "idcomuna,calledireccion,numerodireccion,observaciondireccion) values ( ?, ?, ?, ?, ?, ?)";

                    try (PreparedStatement pst = cn.prepareCall(sql)) {
                        /*para obtener id de producto*/
                        /*se envian los datos a la consulta*/
                        pst.setString(1, txtNombreEpresa.getText()) ;
                        pst.setInt(2, cmbxUsuarios.getItemAt(cmbxUsuarios.getSelectedIndex()).getIdUsuario());
                        pst.setInt(3, cmbxComuna.getItemAt(cmbxComuna.getSelectedIndex()).getIDCOMUNA());
                        pst.setString(4, txtCalleDireccion.getText());
                        pst.setString(5, txtNumero.getText());
                        pst.setString(6,txtObservacion.getText());
                      

                 
                         cn.commit(); 
                        rs = (OracleResultSet) pst.executeQuery();

                        if (rs.next()){
                            limpiarDatos();
                            JOptionPane.showMessageDialog(null, "Datos insertados");
                        }else{
                            JOptionPane.showMessageDialog(null, "Ocurrio un error, no se pudo insertar datos");
                        }
                        //pst.execute();
                    }

                } catch (Exception e) {
                    System.out.println(e.getCause());
                    JOptionPane.showMessageDialog(null,"uups no registro"+e);
                }

            
        }
        else{
            JOptionPane.showMessageDialog(null, "No debe dejar los campos vacios");
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

